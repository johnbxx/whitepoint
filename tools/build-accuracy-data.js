// Generates docs/accuracy-data.js for the accuracy page: per-hue cusp
// boundary residuals (whitepoint exact vs @texel/color's fitted cusp, each
// measured with its OWN conversions — see tools/cusp-accuracy.js for the
// methodology) and full-catalog round-trip errors.
// Usage: node tools/build-accuracy-data.js

import { writeFileSync } from 'node:fs';
import { convert, findCusp, spaces, mulVec } from '../src/index.js';
import * as texel from '@texel/color';

const DEG2RAD = Math.PI / 180;

function wpResidual(h) {
  const cusp = findCusp(h, 'srgb');
  const xyz = convert([cusp[0], cusp[1], h], 'oklch', 'xyz-d65');
  const lin = mulVec(spaces['srgb'].m.fromXyz, xyz);
  return Math.max(Math.abs(Math.min(...lin)), Math.abs(Math.max(...lin) - 1));
}

function texResidual(h) {
  const hr = h * DEG2RAD;
  const cusp = texel.findCuspOKLCH(Math.cos(hr), Math.sin(hr), texel.sRGBGamut, [0, 0]);
  const lin = texel.convert([cusp[0], cusp[1], h], texel.OKLCH, texel.sRGBLinear);
  return Math.max(Math.abs(Math.min(...lin)), Math.abs(Math.max(...lin) - 1));
}

const hues = [];
const wp = [];
const tex = [];
for (let h = 0; h < 360; h += 1) {
  hues.push(h);
  wp.push(wpResidual(h));
  tex.push(texResidual(h));
}

// round-trip errors (srgb → space → srgb), full catalog
function mulberry32(seed) {
  let t = seed >>> 0;
  return function () {
    t = (t + 0x6D2B79F5) >>> 0;
    let r = Math.imul(t ^ (t >>> 15), 1 | t);
    r = (r + Math.imul(r ^ (r >>> 7), 61 | r)) ^ r;
    return ((r ^ (r >>> 14)) >>> 0) / 4294967296;
  };
}
const rand = mulberry32(0x57A71C);
const roundTrip = [];
for (const id of Object.keys(spaces).filter((s) => s !== 'xyz')) {
  let worst = 0;
  for (let n = 0; n < 4000; n++) {
    const c = [0.01 + rand() * 0.98, 0.01 + rand() * 0.98, 0.01 + rand() * 0.98];
    const back = convert(convert(c, 'srgb', id), id, 'srgb');
    for (let i = 0; i < 3; i++) worst = Math.max(worst, Math.abs(c[i] - back[i]));
  }
  roundTrip.push({ id, err: worst });
}
roundTrip.sort((a, b) => a.err - b.err);

const fmt = (a) => `[${a.map((v) => v.toExponential(3)).join(',')}]`;
writeFileSync(new URL('../docs/accuracy-data.js', import.meta.url), `// GENERATED by tools/build-accuracy-data.js — do not edit.
export const cusp = {
  hues: [${hues.join(',')}],
  whitepoint: ${fmt(wp)},
  texel: ${fmt(tex)},
};
export const roundTrip = ${JSON.stringify(roundTrip.map((r) => ({ id: r.id, err: +r.err.toExponential(2) })))};
`);
console.log(`cusp: wp max ${Math.max(...wp).toExponential(2)}, texel max ${Math.max(...tex).toExponential(2)}; roundTrip ${roundTrip.length} spaces`);
