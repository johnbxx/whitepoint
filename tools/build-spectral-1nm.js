// Generates src/spectral/data-1nm.js (CIE 1931 2° CMFs at the normative 1 nm
// resolution, CVRL ciexyz31_1.csv), the CIE F-series SPDs (FL2/FL7/FL11,
// 5 nm), and the CIE high-pressure discharge SPDs (HP1–HP5, 5 nm) — both
// from the CIE 015 dataset via colour-science sds.py. Same provenance
// discipline as build-spectral-data.js.
// Usage: node tools/build-spectral-1nm.js <dir-with-source-files>

import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const dir = process.argv[2] ?? '/tmp';

const rows = readFileSync(join(dir, 'ciexyz31_1.csv'), 'utf8')
  .trim().split('\n')
  .map((l) => l.split(',').map(parseFloat))
  .filter((r) => r.length >= 4 && Number.isFinite(r[0]));

const arr = (a) => `[${a.join(',')}]`;

writeFileSync(new URL('../src/spectral/data-1nm.js', import.meta.url), `// GENERATED by tools/build-spectral-1nm.js — do not edit.
// CIE 1931 2° standard observer CMFs at 1 nm (the CIE 015 normative
// resolution), ${rows[0][0]}–${rows[rows.length - 1][0]} nm, via CVRL.
// Needed for spiky spectra (fluorescent lines, discharge lamps); for smooth
// spectra the default 5 nm tables agree to ~1e-4 in chromaticity.

export const CMF_1931_2_1NM = {
  start: ${rows[0][0]}, step: 1,
  x: ${arr(rows.map((r) => r[1]))},
  y: ${arr(rows.map((r) => r[2]))},
  z: ${arr(rows.map((r) => r[3]))},
};
`);

// F-series SPDs from the colour-science CIE dataset
const py = readFileSync(join(dir, 'cie_sds.py'), 'utf8');
function block(name) {
  const m = py.match(new RegExp(`"${name}": \\{([\\s\\S]*?)\\n    \\}`));
  if (!m) throw new Error(`block ${name} not found`);
  return [...m[1].matchAll(/(\d+(?:\.\d+)?):\s*(-?\d+(?:\.\d+)?)/g)]
    .map(([, wl, v]) => [parseFloat(wl), parseFloat(v)]);
}

const out = ['// GENERATED by tools/build-spectral-1nm.js — do not edit.',
  '// CIE F-series fluorescent illuminant SPDs (CIE 015, 5 nm), via',
  '// colour-science. Spiky line+phosphor spectra — the use case for the',
  '// 1 nm CMFs, and the physics behind the F2/F7/F11 chromaticities the',
  '// illuminant table has carried since the start.', ''];
for (const name of ['FL2', 'FL7', 'FL11']) {
  const e = block(name);
  out.push(`/** CIE ${name.replace('FL', 'F')} fluorescent illuminant relative SPD (CIE 015), 5 nm. */`);
  out.push(`export const ${name}_SPD = {`);
  out.push(`  start: ${e[0][0]}, step: ${e[1][0] - e[0][0]},`);
  out.push(`  values: ${arr(e.map((x) => x[1]))},`);
  out.push(`};`, '');
}
writeFileSync(new URL('../src/spectral/data-fluorescent.js', import.meta.url), out.join('\n'));

// HP-series high-pressure discharge SPDs (CIE 015) from the same dataset.
const HP_DESC = {
  HP1: 'standard high pressure sodium lamp',
  HP2: 'colour-enhanced high pressure sodium lamp',
  HP3: 'high pressure metal halide lamp',
  HP4: 'high pressure metal halide lamp',
  HP5: 'high pressure metal halide lamp',
};
const hp = ['// GENERATED by tools/build-spectral-1nm.js — do not edit.',
  '// CIE high-pressure discharge lamp illuminant SPDs (CIE 015, 5 nm), via',
  '// colour-science. HP1/HP2 are the standardized high-pressure SODIUM lamps —',
  '// the actual CIE sodium illuminant; HP3–HP5 are metal halide. (Low-pressure',
  '// sodium, the near-monochromatic SOX lamp, is NOT a CIE standard — that one',
  '// is modeled in lowPressureSodiumSPD().)', ''];
for (const name of ['HP1', 'HP2', 'HP3', 'HP4', 'HP5']) {
  const e = block(name);
  hp.push(`/** CIE ${name} illuminant relative SPD — ${HP_DESC[name]} (CIE 015), 5 nm. */`);
  hp.push(`export const ${name}_SPD = {`);
  hp.push(`  start: ${e[0][0]}, step: ${e[1][0] - e[0][0]},`);
  hp.push(`  values: ${arr(e.map((x) => x[1]))},`);
  hp.push(`};`, '');
}
writeFileSync(new URL('../src/spectral/data-hp.js', import.meta.url), hp.join('\n'));
console.log(`1nm CMF: ${rows.length} pts; F-series + HP-series written`);
