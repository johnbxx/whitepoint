// Generates src/parse/named.js from the CSS Color 4 specification source.
// No color value in whitepoint is ever typed by hand — this script is the
// provenance chain. Source (fetched 2026-06-12, committed in tools/data/):
//
//  - css-color-4.bs — the W3C csswg-drafts bikeshed source of CSS Color
//    Level 4 (w3c/csswg-drafts), §named-colors table: 148 named colors as
//    <dfn>name</dfn><td>#rrggbb rows.
//
// Usage: node tools/build-named-colors.js

import { readFileSync, writeFileSync } from 'node:fs';

const src = readFileSync(new URL('./data/css-color-4.bs', import.meta.url), 'utf8');

const entries = [...src.matchAll(/<dfn>([a-z]+)<\/dfn><td>(#[0-9a-f]{6})/g)]
  .map(([, name, hex]) => [name, hex]);

// the spec table lists each color once; dedupe defensively and sort
const map = new Map(entries);
if (map.size !== 148) {
  throw new Error(`expected 148 named colors, parsed ${map.size}`);
}

const out = `// CSS Color 4 named colors — GENERATED by tools/build-named-colors.js.
// Do not edit by hand; regenerate from the cited source. Provenance and
// citations are in the generator script.

/** The 148 CSS named colors (CSS Color 4 §6.1), name → #rrggbb. */
export const NAMED_COLORS = {
${[...map.entries()].sort(([a], [b]) => a.localeCompare(b)).map(([n, h]) => `  ${n}: '${h}',`).join('\n')}
};
`;
writeFileSync(new URL('../src/parse/named.js', import.meta.url), out);
console.log(`written: ${map.size} named colors`);
