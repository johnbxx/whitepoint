// Generates src/spectral/data-water.js and src/spectral/data-scotopic.js
// from authoritative source files. No spectral value in whitepoint is ever
// typed by hand — this script is the provenance chain. Sources (fetched
// 2026-06-11, committed in tools/data/):
//
//  - popefry.dat — R. M. Pope and E. S. Fry, "Absorption spectrum
//    (380–700 nm) of pure water. II. Integrating cavity measurements,"
//    Appl. Opt. 36, 8710–8723 (1997). 380–727.5 nm at 2.5 nm, in 1/cm,
//    via OMLC (omlc.org/spectra/water). Converted to 1/m by exact
//    decimal-point shift — the values never pass through arithmetic.
//  - scvle_1.csv — CIE (1951) scotopic luminous efficiency V′(λ),
//    380–780 nm at 1 nm, via CVRL (cvrl.org).
//
// Usage: node tools/build-demo-spectra.js

import { readFileSync, writeFileSync } from 'node:fs';

const dir = new URL('./data/', import.meta.url);

// ×100 (1/cm → 1/m) by shifting the decimal point in the source string:
// exact by construction, no float roundtrip.
function shift100(s) {
  s = s.trim();
  if (!/^\d+(\.\d+)?$/.test(s)) throw new Error(`unexpected number format: ${s}`);
  let [int, frac = ''] = s.split('.');
  while (frac.length < 2) frac += '0';
  int = (int + frac.slice(0, 2)).replace(/^0+(?=\d)/, '');
  frac = frac.slice(2);
  return String(parseFloat(frac ? `${int}.${frac}` : int));
}

// ---- Pope & Fry water absorption ----
const pf = readFileSync(new URL('popefry.dat', dir), 'utf8')
  .split('\n')
  .map((line) => line.trim().split(/\s+/))
  .filter((t) => t.length === 2 && /^\d/.test(t[0]));
const pfStart = parseFloat(pf[0][0]);
const pfStep = parseFloat(pf[1][0]) - pfStart;
for (let i = 1; i < pf.length; i++) {
  if (Math.abs(parseFloat(pf[i][0]) - pfStart - i * pfStep) > 1e-9) {
    throw new Error(`non-uniform water grid at row ${i}`);
  }
}

const waterOut = `// Pure-water absorption spectrum — GENERATED by tools/build-demo-spectra.js.
// Do not edit by hand; regenerate from the cited source. Provenance and
// citations are in the generator script.

/**
 * Spectral absorption coefficient of pure water, 1/m, ${pfStep} nm,
 * ${pfStart}–${pfStart + pfStep * (pf.length - 1)} nm. Pope & Fry (1997), Appl. Opt. 36, 8710,
 * integrating-cavity measurements (via OMLC). Beer–Lambert ready:
 * T(λ, d) = exp(−a(λ)·d) with d in meters.
 */
export const WATER_ABSORPTION = {
  start: ${pfStart}, step: ${pfStep},
  values: [${pf.map((t) => shift100(t[1])).join(', ')}],
};
`;
writeFileSync(new URL('../src/spectral/data-water.js', import.meta.url), waterOut);

// ---- CIE 1951 scotopic V′(λ) ----
const sv = readFileSync(new URL('scvle_1.csv', dir), 'utf8')
  .trim().split('\n')
  .map((line) => line.split(',').map((v) => parseFloat(v)))
  .filter((r) => r.length >= 2 && Number.isFinite(r[0]));
const svStart = sv[0][0];
const svStep = sv[1][0] - svStart;

const scotopicOut = `// CIE 1951 scotopic luminous efficiency — GENERATED by
// tools/build-demo-spectra.js. Do not edit by hand; regenerate from the
// cited source. Provenance and citations are in the generator script.

/**
 * CIE (1951) scotopic luminous efficiency function V′(λ), ${svStep} nm,
 * ${svStart}–${svStart + svStep * (sv.length - 1)} nm (CVRL). Peak at 507 nm; the rod sensitivity
 * curve. Shaped like a CMF y-channel for use with the same samplers.
 */
export const V_PRIME_1951 = {
  start: ${svStart}, step: ${svStep},
  values: [${sv.map((r) => r[1]).join(', ')}],
};
`;
writeFileSync(new URL('../src/spectral/data-scotopic.js', import.meta.url), scotopicOut);

console.log(`written: water ${pf.length} pts (${pfStart}–${pfStart + pfStep * (pf.length - 1)} nm, 1/m), V' ${sv.length} pts`);
