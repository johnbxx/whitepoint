// Generates src/sky/data.js from the Hosek–Wilkie reference distribution.
// No coefficient in whitepoint is ever typed by hand — this script is the
// provenance chain. Source (fetched 2026-06-11, committed in tools/data/):
//
//  - ArHosekSkyModelData_Spectral.h — L. Hosek and A. Wilkie, "An Analytic
//    Model for Full Spectral Sky-Dome Radiance," SIGGRAPH 2012. Reference
//    implementation v1.4a (3-clause BSD, © 2012–2013 Lukas Hosek and
//    Alexander Wilkie, Charles University in Prague). 11 wavelength bands
//    (320–720 nm, 40 nm): per band 1080 sky-dome coefficients
//    (2 albedos × 10 turbidities × 6 elevation control points × 9 params)
//    and 120 radiance-normalization values (2 × 10 × 6).
//
// Coefficients are copied as verbatim source literals — they never pass
// through arithmetic or reformatting.
//
// Usage: node tools/build-sky-data.js

import { readFileSync, writeFileSync } from 'node:fs';

const src = readFileSync(new URL('./data/ArHosekSkyModelData_Spectral.h', import.meta.url), 'utf8');

function block(name, expectLen) {
  const m = src.match(new RegExp(`double ${name}\\[\\]\\s*=\\s*\\{([\\s\\S]*?)\\};`));
  if (!m) throw new Error(`block ${name} not found`);
  const body = m[1].replace(/\/\/[^\n]*/g, '');
  const tokens = body.match(/-?\d+(?:\.\d+)?(?:e[+-]?\d+)?/gi);
  if (tokens.length !== expectLen) {
    throw new Error(`${name}: ${tokens.length} values, expected ${expectLen}`);
  }
  // verbatim literals; JS parses C's 1.234567e-001 exponent form natively
  return tokens;
}

const WL = [320, 360, 400, 440, 480, 520, 560, 600, 640, 680, 720];
const datasets = WL.map((wl) => block(`dataset${wl}`, 1080));
const radiances = WL.map((wl) => block(`datasetRad${wl}`, 120));

const out = `// Hosek–Wilkie spectral sky-dome coefficients — GENERATED by
// tools/build-sky-data.js. Do not edit by hand; regenerate from the cited
// source. Provenance, license, and citations are in the generator script.
//
// Data © 2012–2013 Lukas Hosek and Alexander Wilkie, Charles University in
// Prague (3-clause BSD; see tools/data/ArHosekSkyModelData_Spectral.h).

/** Band wavelengths, nm. */
export const SKY_WL = [${WL.join(', ')}];

/**
 * Sky-dome coefficient datasets, one per band. Layout (C order):
 * [albedo 0|1][turbidity 1–10][elevation control point 0–5][param 0–8].
 */
export const SKY_DATASETS = [
${datasets.map((d) => `  [${d.join(', ')}],`).join('\n')}
];

/** Radiance normalization datasets, one per band: [albedo][turbidity][cp]. */
export const SKY_RADIANCES = [
${radiances.map((d) => `  [${d.join(', ')}],`).join('\n')}
];
`;

writeFileSync(new URL('../src/sky/data.js', import.meta.url), out);

// ---- solar disc: direct radiance splines + limb darkening ----
const solar = WL.map((wl) => block(`solarDataset${wl}`, 1800)); // 4 order × 45 pieces × 10 turbidities
const limb = WL.map((wl) => block(`limbDarkeningDataset${wl}`, 6));

const solarOut = `// Hosek–Wilkie solar-disc coefficients — GENERATED by
// tools/build-sky-data.js. Do not edit by hand; regenerate from the cited
// source. Provenance, license, and citations are in the generator script.
//
// Data © 2012–2013 Lukas Hosek and Alexander Wilkie, Charles University in
// Prague (3-clause BSD; see tools/data/ArHosekSkyModelData_Spectral.h).

/**
 * Direct solar radiance datasets, one per band (320–720 nm at 40 nm).
 * Layout (C order): [turbidity 1–10][elevation piece 0–44][cubic coef 0–3],
 * a piecewise cubic in cube-root-transformed solar elevation.
 */
export const SOLAR_DATASETS = [
${solar.map((d) => `  [${d.join(', ')}],`).join('\n')}
];

/** Limb-darkening polynomial coefficients (5th order), one sextet per band. */
export const LIMB_DARKENING = [
${limb.map((d) => `  [${d.join(', ')}],`).join('\n')}
];
`;
writeFileSync(new URL('../src/sky/data-solar.js', import.meta.url), solarOut);

console.log(`written: ${WL.length} bands × (1080 + 120) sky + (1800 + 6) solar coefficients, verbatim`);
