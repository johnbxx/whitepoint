// Generates src/spectral/data.js from authoritative source files.
// No spectral value in whitepoint is ever typed by hand — this script is the
// provenance chain. Sources (fetched 2026-06-11):
//
//  - ciexyz31.csv, ciexyz64.csv  — CIE 1931 2° / 1964 10° standard observer
//    CMFs at 5 nm, 360–830 nm, via CVRL (cvrl.org), the Colour & Vision
//    Research Laboratory mirror of the CIE tables.
//  - Illuminantd65.csv — CIE standard illuminant D65 relative SPD at 1 nm,
//    300–830 nm, via CVRL.
//  - sds_d_illuminant_series.py — CIE daylight basis functions S0/S1/S2 at
//    10 nm, 360–830 nm (CIE 015, Table 6), via colour-science.
//
// Usage: node tools/build-spectral-data.js <dir-with-source-files>

import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const dir = process.argv[2] ?? '/tmp';

function parseCsv(file) {
  return readFileSync(join(dir, file), 'utf8')
    .trim().split('\n')
    .map((line) => line.split(',').map((v) => parseFloat(v)));
}

function cmfTable(file) {
  const rows = parseCsv(file).filter((r) => r.length >= 4 && Number.isFinite(r[0]));
  const start = rows[0][0];
  const step = rows[1][0] - rows[0][0];
  return {
    start, step,
    x: rows.map((r) => r[1]),
    y: rows.map((r) => r[2]),
    z: rows.map((r) => r[3]),
  };
}

const cmf31 = cmfTable('ciexyz31.csv');
const cmf64 = cmfTable('ciexyz64.csv');

const d65rows = parseCsv('d65.csv').filter((r) => r.length >= 2 && Number.isFinite(r[0]));
const d65 = {
  start: d65rows[0][0],
  step: d65rows[1][0] - d65rows[0][0],
  values: d65rows.map((r) => r[1]),
};

// S0/S1/S2 from the colour-science python source: blocks of "wl: value,"
const py = readFileSync(join(dir, 's0s1s2.py'), 'utf8');
function pyBlock(name) {
  const m = py.match(new RegExp(`"${name}": \\{([\\s\\S]*?)\\}`));
  if (!m) throw new Error(`S-component block ${name} not found`);
  const entries = [...m[1].matchAll(/(\d+(?:\.\d+)?):\s*(-?\d+(?:\.\d+)?)/g)]
    .map(([, wl, v]) => [parseFloat(wl), parseFloat(v)]);
  return entries;
}
const s0e = pyBlock('S0'), s1e = pyBlock('S1'), s2e = pyBlock('S2');
const sStart = s0e[0][0], sStep = s0e[1][0] - s0e[0][0];

const arr = (a) => `[${a.join(', ')}]`;

const out = `// CIE spectral datasets — GENERATED by tools/build-spectral-data.js.
// Do not edit by hand; regenerate from the cited sources. Provenance and
// citations are in the generator script.

/** CIE 1931 2° standard observer CMFs, ${cmf31.step} nm, ${cmf31.start}–${cmf31.start + cmf31.step * (cmf31.x.length - 1)} nm (CVRL). */
export const CMF_1931_2 = {
  start: ${cmf31.start}, step: ${cmf31.step},
  x: ${arr(cmf31.x)},
  y: ${arr(cmf31.y)},
  z: ${arr(cmf31.z)},
};

/** CIE 1964 10° standard observer CMFs, ${cmf64.step} nm (CVRL). */
export const CMF_1964_10 = {
  start: ${cmf64.start}, step: ${cmf64.step},
  x: ${arr(cmf64.x)},
  y: ${arr(cmf64.y)},
  z: ${arr(cmf64.z)},
};

/** CIE standard illuminant D65 relative SPD, ${d65.step} nm, ${d65.start}–${d65.start + d65.step * (d65.values.length - 1)} nm (CVRL). */
export const D65_SPD = {
  start: ${d65.start}, step: ${d65.step},
  values: ${arr(d65.values)},
};

/** CIE daylight basis functions (CIE 015 Table 6), ${sStep} nm. */
export const DAYLIGHT_S = {
  start: ${sStart}, step: ${sStep},
  s0: ${arr(s0e.map((e) => e[1]))},
  s1: ${arr(s1e.map((e) => e[1]))},
  s2: ${arr(s2e.map((e) => e[1]))},
};
`;

writeFileSync(new URL('../src/spectral/data.js', import.meta.url), out);
console.log(`written: CMF31 ${cmf31.x.length} pts, CMF64 ${cmf64.x.length} pts, D65 ${d65.values.length} pts, S ${s0e.length} pts`);
