// Generates src/spectral/data-tcs.js and src/spectral/data-ces.js from
// authoritative source files. No spectral value in whitepoint is ever typed
// by hand — this script is the provenance chain. Sources (fetched
// 2026-06-12, committed in tools/data/):
//
//  - tcs.py — the CIE 13.3-1995 test colour samples TCS01–TCS14 spectral
//    radiance factors, 360–830 nm at 5 nm, via colour-science (BSD-3),
//    block DATA_TCS_CIE1995.
//  - tcs_cfi2017_5_nm.csv.gz — the 99 colour evaluation samples (CES) of
//    CIE 224:2017 / IES TM-30, 380–780 nm at 5 nm, via colour-science.
//    (The 1 nm original is also committed; the 5 nm abridgment shifts Rf
//    by < 0.1 on the smooth F-series anchors — measured, not assumed.)
//
// Usage: node tools/build-quality-data.js

import { readFileSync, writeFileSync } from 'node:fs';
import { gunzipSync } from 'node:zlib';

const dir = new URL('./data/', import.meta.url);

// ---- CIE 13.3 TCS01–14 ----
const py = readFileSync(new URL('tcs.py', dir), 'utf8');
const block1995 = py.match(/^DATA_TCS_CIE1995: dict = \{[\s\S]*?\n\}/m)[0];
const tcs = [];
for (let i = 1; i <= 14; i++) {
  const key = `TCS${String(i).padStart(2, '0')}`;
  const m = block1995.match(new RegExp(`"${key}": \\{([\\s\\S]*?)\\}`));
  if (!m) throw new Error(`${key} not found in DATA_TCS_CIE1995`);
  const entries = [...m[1].matchAll(/(\d+(?:\.\d+)?):\s*(-?\d+(?:\.\d+)?)/g)]
    .map(([, wl, v]) => [parseFloat(wl), v]);
  if (i === 1) {
    tcs.start = entries[0][0];
    tcs.step = entries[1][0] - entries[0][0];
    tcs.n = entries.length;
  } else if (entries.length !== tcs.n) {
    throw new Error(`${key}: ${entries.length} points, expected ${tcs.n}`);
  }
  tcs.push(entries.map((e) => e[1]));
}

const tcsOut = `// CIE 13.3-1995 test colour samples — GENERATED by
// tools/build-quality-data.js. Do not edit by hand; regenerate from the
// cited source. Provenance and citations are in the generator script.

/**
 * Spectral radiance factors of the CIE 13.3-1995 test colour samples
 * TCS01–TCS14, ${tcs.step} nm, ${tcs.start}–${tcs.start + tcs.step * (tcs.n - 1)} nm (via colour-science).
 * Samples 1–8 are the Ra set; 9–14 are the specials (R9 = strong red).
 */
export const TCS_CIE1995 = {
  start: ${tcs.start}, step: ${tcs.step},
  samples: [
${tcs.map((s) => `    [${s.join(', ')}],`).join('\n')}
  ],
};
`;
writeFileSync(new URL('../src/spectral/data-tcs.js', import.meta.url), tcsOut);

// ---- CIE 224:2017 / TM-30 99 CES ----
const csv = gunzipSync(readFileSync(new URL('tcs_cfi2017_5_nm.csv.gz', dir)))
  .toString('utf8').trim().split('\n')
  .map((line) => line.split(','));
const cesStart = parseFloat(csv[0][0]);
const cesStep = parseFloat(csv[1][0]) - cesStart;
const nCes = csv[0].length - 1;
if (nCes !== 99) throw new Error(`expected 99 CES, got ${nCes}`);
// columns are samples: transpose, keeping values verbatim
const ces = Array.from({ length: nCes }, (_, s) => csv.map((row) => row[s + 1]));

const cesOut = `// CIE 224:2017 / IES TM-30 colour evaluation samples — GENERATED by
// tools/build-quality-data.js. Do not edit by hand; regenerate from the
// cited source. Provenance and citations are in the generator script.

/**
 * Spectral radiance factors of the 99 colour evaluation samples (CES) of
 * CIE 224:2017 and IES TM-30, ${cesStep} nm, ${cesStart}–${cesStart + cesStep * (csv.length - 1)} nm (via colour-science).
 */
export const CES_CIE2017 = {
  start: ${cesStart}, step: ${cesStep},
  samples: [
${ces.map((s) => `    [${s.join(', ')}],`).join('\n')}
  ],
};
`;
writeFileSync(new URL('../src/spectral/data-ces.js', import.meta.url), cesOut);

console.log(`written: ${tcs.length} TCS (${tcs.n} pts), ${nCes} CES (${csv.length} pts)`);
